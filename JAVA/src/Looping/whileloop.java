package Looping;

class whileloop {
	public static void main(String[] args) {
		int a=5;
		while(a>=1){
			System.out.println(a);
			a--;
		}
		System.out.println("----------");

		int b=1;
		while(b<=5){ 
			System.out.println(b);
			b++;

		}
		
		System.out.println("------");
		
		int c=5;
		while(c<=25){
			System.out.println(c);
			c+=5;
		}
		
	}

}
